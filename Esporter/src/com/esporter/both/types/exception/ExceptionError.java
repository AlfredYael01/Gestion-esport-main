package com.esporter.both.types.exception;

public class ExceptionError extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7257124303406974369L;


	public ExceptionError() {
		super();
	}

	public ExceptionError(String message, Throwable cause) {
		super(message, cause);
	}

	public ExceptionError(String message) {
		super(message);
	}

	public ExceptionError(Throwable cause) {
		super(cause);
	}
	
	

}
